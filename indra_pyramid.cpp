	int m,i,j,result = 0;

	cin >> m;

	vector<pair <int,int> > blocks;

	for(int k = 0; k < m; k++){
		cin >> i >> j;

		if(i <= j){
			blocks.push_back(make_pair(i, j));
		}
		else{
			blocks.push_back(make_pair(j, i));
		}
	}

	sort(all(blocks));

	for(int k = 0; k < m; k++){

		pair <int,int> temp = blocks[k];

		if(result < temp.first){
			result++;
		}
	}

	cout << result ;

	
	return 0;
